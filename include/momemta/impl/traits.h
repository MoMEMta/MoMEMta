#include <string>
#include <type_traits>

template <typename T>
struct is_string: public std::integral_constant<bool,
        std::is_same<      char *, typename std::decay<T>::type>::value ||
        std::is_same<const char *, typename std::decay<T>::type>::value
     > {};

template <>
struct is_string<std::string>: std::true_type {};
